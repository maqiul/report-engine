    // ============== ConditionalFormats ==============

    [Fact]
    public void ConditionalFormats_EmptyByDefault()
    {
        var el = new TestReportElement();
        Assert.Empty(el.ConditionalFormats);
    }

    [Fact]
    public void ConditionalFormats_Add_Works()
    {
        var el = new TestReportElement();
        el.ConditionalFormats.Add(new ConditionalFormatRule
        {
            Expression = "[Amount] > 1000",
            BackgroundColor = "#FF0000"
        });
        Assert.Single(el.ConditionalFormats);
    }

    [Fact]
    public void ConditionalFormats_AddMultiple_Works()
    {
        var el = new TestReportElement();
        el.ConditionalFormats.Add(new ConditionalFormatRule { Expression = "[Amount] > 1000" });
        el.ConditionalFormats.Add(new ConditionalFormatRule { Expression = "[Amount] < 100" });
        Assert.Equal(2, el.ConditionalFormats.Count);
    }

    // ============== 综合场景 ==============

    [Fact]
    public void ReportElement_FullSetup_Works()
    {
        var el = new TestReportElement
        {
            Name = "amount",
            GroupId = "group1",
            X = 10,
            Y = 20,
            Width = 100,
            Height = 30,
            BackgroundColor = "#EEEEEE",
            Border = new BorderDef { Width = 1 },
            Visible = true,
            Locked = false,
            Rotation = 0,
            Opacity = 0.8
        };
        el.ConditionalFormats.Add(new ConditionalFormatRule
        {
            Expression = "[Amount] > 1000",
            FontColor = "#FF0000",
            Bold = true
        });

        Assert.Equal("amount", el.Name);
        Assert.Equal("group1", el.GroupId);
        Assert.Equal(10, el.X);
        Assert.Equal(0.8, el.Opacity);
        Assert.Single(el.ConditionalFormats);
    }

    [Fact]
    public void ReportElement_InBand_Works()
    {
        var band = new Band { Type = BandType.Detail, Height = 50 };
        band.Elements.Add(new TestReportElement { Name = "field1", X = 10, Y = 5, Width = 80, Height = 20 });
        band.Elements.Add(new TestReportElement { Name = "field2", X = 100, Y = 5, Width = 80, Height = 20 });

        Assert.Equal(2, band.Elements.Count);
        Assert.Equal("field1", band.Elements[0].Name);
        Assert.Equal("field2", band.Elements[1].Name);
    }

