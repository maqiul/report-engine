    [Fact]
    public void Opacity_DefaultIs1()
    {
        var el = new TestReportElement();
        Assert.Equal(1.0, el.Opacity);
    }

    [Fact]
    public void Opacity_SetHalf_Works()
    {
        var el = new TestReportElement { Opacity = 0.5 };
